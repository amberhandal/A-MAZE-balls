class Robot():
    def __init__(self, x, y, isfinish):
        self.x = x
        self.y = y
        self.isfinish = isfinish

    def __str__(self):
        return f"I'm at {self.x} {self.y}. My state is {isfinish}. Bye Bye Silly Goose."
    
    def set_position(x, y):
        self.x = x
        self.y = y

    